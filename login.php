<?php
// ============================================================
// صفحة تسجيل دخول الإداري — Hevan Booking
// ============================================================
// دي الصفحة اللي بتظهر للمدير عشان يدخل اسم المستخدم وكلمة
// المرور ويقدر يوصل للوحة التحكم (Admin Dashboard).
// ============================================================

// --- استدعاء ملف المصادقة ---
// هنا جبنا ملف auth.php اللي فيه دوال تسجيل الدخول والتحقق
require_once __DIR__ . '/auth.php';

// --- متغيرات الصفحة ---
// $title: عنوان الصفحة اللي بيظهر في تبويب المتصفح
// $error: رسالة الخطأ (لو حصل خطأ في الدخول) — فاضي في البداية
$title = 'تسجيل دخول الإداري';
$error = '';

// --- لو المستخدم داخل بالفعل ---
// نستخدم الدالة is_admin() اللي ترجع true لو المستخدم مسجل دخول.
// لو هو داخل نوجهه فوراً للوحة التحكم بدون ما نشوف صفحة الدخول.
if (is_admin()) {
    redirect('admin/index.php');
}

// --- معالجة نموذج الدخول (بعد الضغط على زر "دخول") ---
// بنتأكد من إن الطلب POST (يعني المستخدم ضغط على الزر وجاب البيانات)
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // --- جيب البيانات اللي المستخدم كتبها ---
    // نستخدم trim() عشان نمسح أي مسافات زايدة قبل وبعد النص
    $username = trim($_POST['username'] ?? '');
    $password = trim($_POST['password'] ?? '');

    // --- حاول تسجيل الدخول ---
    // login_user() بتجيب المستخدم من قاعدة البيانات وتتأكد من كلمة المرور
    if (login_user($username, $password)) {
        // لو صح: وجه المستخدم إلى لوحة التحكم
        redirect('admin/index.php');
    } else {
        // لو غلط: حط رسالة الخطأ عشان تظهر للمستخدم
        $error = '❌ اسم المستخدم أو كلمة المرور غير صحيحة';
    }
}

// --- جلب الهيدر (القسم العلوي) ---
// هنا نجيب الهيدر اللي فيه opening tags, الـ CSS, الـ navbar ..إلخ
include __DIR__ . '/includes/header.php';
?>

<!-- ============================================================
     بطاقة تسجيل الدخول
     ============================================================
     دي البطاقة اللي فيها نموذج الدخول بتظهر للمستخدم.
     القيد style:max-width=480px يخليها وسط الصفحة وما تكون
     واسعة زيادة.
     ============================================================ -->
<div class="card form-card" style="max-width:480px;margin:40px auto">
  <h1><?= svg_icon('lock') ?> تسجيل دخول الإداري</h1>

  <!-- ---- رسالة تذكير ببيانات الدخول التجريبية ---- -->
  <div class="alert info">
    <?= svg_icon('bulb') ?> بيانات الدخول التجريبية:<br>
    المستخدم: <b>admin</b> &nbsp;|&nbsp; كلمة المرور: <b>admin123</b>
  </div>

  <!-- ---- رسالة الخطأ (لو حصل خطأ في الدخول) ---- -->
  <?php if ($error): ?>
    <!-- الدالة h() بتأمن النص عشان ما يكون فيه ثغرات XSS -->
    <div class="alert error"><?= h($error) ?></div>
  <?php endif; ?>

  <!-- ---- نموذج الدخول ---- -->
  <form method="post">
    <!-- حقل اسم المستخدم -->
    <label for="username"><?= svg_icon('user') ?> اسم المستخدم</label>
    <input type="text" id="username" name="username" required autofocus>

    <!-- حقل كلمة المرور -->
    <label for="password"><?= svg_icon('key') ?> كلمة المرور</label>
    <input type="password" id="password" name="password" required>

    <!-- زر الإرسال -->
    <div class="form-actions" style="margin-top:20px">
      <button class="btn" type="submit"><?= svg_icon('lock') ?> دخول</button>
    </div>
  </form>
</div>

<!-- ============================================================
     جلب الفوتر (القسم السفلي)
     ============================================================
     هنا نجيب الفوتر اللي فيه closing tags والجافاسكريبت.
     ============================================================ -->
<?php include __DIR__ . '/includes/footer.php'; ?>

<!DOCTYPE html>
<!--
    ============================================================
    includes/header.php
    ============================================================
    دا الجزء المشترك من كل صفحة في الموقع — الهيدر اللي بيظهر فوق
    في كل الصفحات. فيه الـ HTML structure الأساسية، الـ nav بار،
    والـ brand بتاع الموقع.
-->
<html lang="ar" dir="rtl">
<!--
    اللغة عربي (lang="ar") والاتجاه من اليمين لليسار (dir="rtl")
    عشان الموقع مكتوب بالعربي و السودانيين يقراوه بسهولة.
-->
<head>
  <!--
      الـ head فيه الميتا والستايلات والسكريبتات اللي بتشتغل
      قبل ما الصفحة تتحمل كلها.
  -->
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <!--
      عنوان الصفحة: لو صفحة معينة مرّرت $title بيظهر،
      ولو لا بيظهر العنوان الافتراضي (نظام الحجوزات السياحية).
  -->
  <title><?= h($title ?? 'نظام الحجوزات السياحية') ?> | Hevan Booking</title>
  <script>
    // ============================================================
    // كود الدارك مود — يشتغل قبل تحميل الصفحة عشان يضبط السمة
    // قبل ما المحتوى يظهر، عشان كدا حطيناه في الـ <head>.
    // ============================================================
    (function () {
      try {
        // نقرا السمة المحفوظة من localStorage.
        // لو ما في سمة محفوظة، نستخدم 'light' كافتراضي.
        var theme = localStorage.getItem('hevan-theme') || 'light';
        // نحط data-theme على عنصر <html> عشان الـ CSS يشتغل عليه.
        document.documentElement.setAttribute('data-theme', theme);
      } catch (e) {
        // لو localStorage مش متاح (زي في متصفحات قديمة)،
        // نتجاهل الخطأ ونستمر بالسمة الافتراضية.
      }
    })();
  </script>
  <!--
      نشيل ملف الـ style.css اللي فيه كل التنسيقات.
      asset_href() بتجيب المسار الصحيح للملف سواء كنا
      في Docker أو XAMPP.
  -->
  <link rel="stylesheet" href="<?= h(asset_href('style.css')) ?>">
</head>
<body>

<!--
    ============================================================
    الـ topbar — الشريط العلوي الثابت
    ============================================================
    دا الشريط اللي في فوق الموقع كله، فيه اللوجو والروابط
    وزر الدارك مود. كل حاجة جوا container عشان تكون
    متناسقة مع باقي الموقع.
-->
<div class="topbar">
  <div class="container">
    <!--
        الـ nav بيحتوي على جزئين رئيسيين:
        1- البراند (اللوجو + اسم الموقع)
        2- الروابط وزر التبديل
    -->
    <div class="nav">
      <!--
          ============================================================
          البراند (Brand)
          ============================================================
          الرابط دا بيودّي على الصفحة الرئيسية، وفيه اللوجو
          (صورة) وأيقونة الكرة الأرضية واسم "Hevan Booking".
      -->
      <a href="<?= url('index.php') ?>" class="brand">
      <!--
          اللوجو: صورة بمساحة 40×40. لو الصورة ما تحملتش
          (onerror) نخفيها عشان ما يظهر مربع فارغ.
      -->
      <img class="brand-logo" src="<?= asset_url('logo/logoicon1.png') ?>" alt="Hevan Booking" onerror="this.style.display='none'" width="40" height="40">
      <?= svg_icon('globe') ?> Hevan Booking
    </a>
      <!--
          ============================================================
          قائمة الـ navigation — روابط التنقل
          ============================================================
      -->
      <nav>
        <!-- الرابط: الصفحة الرئيسية -->
        <a href="<?= url('index.php') ?>">الرئيسية</a>
        <!-- الرابط: حجز جديد — أي زائر يقدر يستخدمه -->
        <a href="<?= url('book.php') ?>">حجز جديد</a>
        <?php if (is_admin()): ?>
          <!--
              ============================================================
              روابط تظهر بس للإدمن
              ============================================================
              لو المستخدم مسجل دخول كإدمن (is_admin() == true)،
              تظهر رابطة "لوحة التحكم" ورابط "تسجيل خروج".
          -->
          <!-- الرابط: لوحة التحكم (admin dashboard) -->
          <a href="<?= url('admin/index.php') ?>">لوحة التحكم</a>
          <!-- الرابط: تسجيل خروج من حساب الإدمن -->
          <a href="<?= url('logout.php') ?>">تسجيل خروج</a>
        <?php else: ?>
          <!--
              ============================================================
              رابط يظهر للزوار (غير الإدمن)
              ============================================================
              لو المستخدم ما سجّل دخول، يظهر له رابط "دخول الإداري"
              عشان يقدر يسجل دخول لو عاوز.
          -->
          <a href="<?= url('login.php') ?>">دخول الإداري</a>
        <?php endif; ?>
        <!--
            ============================================================
            زر الدارك مود (Dark Mode Toggle)
            ============================================================
            الزر دا بيبدّل بين الوضع الفاتح والوضع الغامق.
            فيه أيقونتين:
            - moon (القمر): تظهر في الوضع الفاتح
            - sun (الشمس): تظهر في الوضع الغامق
            الـ CSS هو اللي يتحكم في إخفاء/إظهار كل أيقونة
            حسب السمة الحالية.
        -->
        <button id="themeToggle" class="theme-toggle" type="button" aria-label="تبديل الوضع">
          <!-- أيقونة القمر (تظهر في النهار/الوضع الفاتح) -->
          <?= svg_icon('moon', 'theme-icon-moon') ?>
          <!-- أيقونة الشمس (تظهر في الليل/الوضع الغامق) -->
          <?= svg_icon('sun', 'theme-icon-sun') ?>
        </button>
      </nav>
    </div>
  </div>
</div>

<!--
    ============================================================
    container.page — الحاوية الرئيسية للمحتوى
    ============================================================
    كل صفحة تبدأ بعد الهيدر مباشرة، والمحتوى بييجي جوا
    container.page. دا فتح div بس، وكل صفحة بتكمله
    (يعني ما فيش </div> هنا عشان كل صفحة تقفل بنفسها).
-->
<div class="container page">

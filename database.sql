-- ------------------------------------------------------------
-- 1. قاعدة البيانات
-- ------------------------------------------------------------
-- هنا بننشئ قاعدة البيانات نفسها (Database).
-- لو كانت موجودة قبل كدا بنستخدم IF NOT EXISTS عشان ما تنشأ تاني ونحصل خطأ.
-- الترميز utf8mb4 بيدعم العربية والمشاعر (إيموجي) — تمام للحجز السياحي.
-- ------------------------------------------------------------
CREATE DATABASE IF NOT EXISTS hevan_booking
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

-- نختار قاعدة البيانات عشان نشتغل عليها من الآن.
USE hevan_booking;

-- ------------------------------------------------------------
-- 2. جدول المستخدمين (الإداريون فقط)
-- ------------------------------------------------------------
-- هذا الجدول خاص بالإداريين والمشرفين على الموقع (Admin / Manager).
-- ما فيه حسابات زبائن — الزبائن يحجزوا من الموقع المفتوح بدون تسجيل.
-- الفكرة: كل إداري عنده يوزر وباسوورد (مشفّر bcrypt)، ودوره شنهو.
-- ------------------------------------------------------------
DROP TABLE IF EXISTS `users`;

CREATE TABLE `users` (
  `id`          INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  -- رقم تسلسلي أتوماتيكي لكل مستخدم
  `full_name`   VARCHAR(150) NOT NULL,
  -- الاسم الكامل للإداري
  `username`    VARCHAR(50)  NOT NULL UNIQUE,
  -- اسم الدخول — لازم يكون فريد عشان ما يتضارب
  `password`    VARCHAR(255) NOT NULL COMMENT 'bcrypt hashed',
  -- الباسوورد مشفر بـ bcrypt مش نص عادي — أمان
  `email`       VARCHAR(150) NOT NULL,
  -- الإيميل للمراسلة
  `role`        ENUM('admin','manager') DEFAULT 'admin',
  -- نوع المستخدم: Admin (كامل صلاحيات) أو Manager (محدود)
  `created_at`  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  -- تاريخ التسجيل — ينحط أتوماتيكي
  `updated_at`  TIMESTAMP NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP
  -- تاريخ آخر تعديل — بيتحدث لو حصل UPDATE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- InnoDB عشان يدعم العلاقات بين الجداول (Foreign Keys)، والترميز utf8mb4 عشان العربي

-- ------------------------------------------------------------
-- 3. جدول الشركات السياحية
-- ------------------------------------------------------------
-- هنا بنخزّن الشركات اللي بتقدم الخدمات السياحية.
-- كل شركة ليها بيانات تواصل: إيميل، تلفون، عنوان.
-- في خاصية is_active عشان تفعيل/إلغاء الشركة من الظهور.
-- اللوجو مسار صورة — ما بنخزّن الصورة نفسها في قاعدة البيانات.
-- ------------------------------------------------------------
DROP TABLE IF EXISTS `companies`;

CREATE TABLE `companies` (
  `id`           INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  -- رقم تسلسلي للشركة
  `name`         VARCHAR(150) NOT NULL,
  -- اسم الشركة
  `description`  TEXT DEFAULT NULL,
  -- وصف الشركة ونشاطها
  `email`        VARCHAR(150) DEFAULT NULL,
  `phone`        VARCHAR(30)  DEFAULT NULL,
  `address`      VARCHAR(255) DEFAULT NULL,
  `logo`         VARCHAR(255) DEFAULT NULL COMMENT 'مسار صورة الشعار',
  -- مسار الصورة مش الصورة نفسها — عشان حفظ مساحة
  `is_active`    TINYINT(1) UNSIGNED NOT NULL DEFAULT 1,
  -- 1 = الشركة شغالة وتظهر في الموقع، 0 = مخفية
  `created_at`   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at`   TIMESTAMP NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  INDEX `idx_companies_active` (`is_active`)
  -- Index عشان البحث عن الشركات النشطة يكون سريع
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 4. جدول الأماكن / الوجهات السياحية
-- ------------------------------------------------------------
-- هذا الجدول الأهم في النظام: فيه الوجهات اللي الزبون يقدر يحجزها.
-- كل مكان تابع لشركة (علاقة: Companies -> Places).
-- السعر بالليلة، وعدد الضيوف الأقصى، والتصنيف (فندق/شقة/منتجع/الخ).
-- التقييم rating من 0.00 إلى 5.00 (مثلاً 4.70).
-- is_featured عشان تظهر في قسم "مقترح" في الصفحة الرئيسية.
-- العلاقة foreign key تربط المكان بالشركة — لو انمسحت الشركة، مكانها يصير NULL.
-- ------------------------------------------------------------
DROP TABLE IF EXISTS `places`;

CREATE TABLE `places` (
  `id`             INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `company_id`     INT UNSIGNED NOT NULL,
  -- رقم الشركة التابع ليها المكان — علاقة (Many-to-One)
  `name`           VARCHAR(150) NOT NULL,
  -- اسم الوجهة (مثلاً: فندق القدس الوطني)
  `description`    TEXT DEFAULT NULL,
  -- وصف تفصيلي للمكان
  `location`       VARCHAR(255) NOT NULL COMMENT 'موقع تقريبي / عنوان',
  -- عنوان تقريبي (مش GPS)
  `city`           VARCHAR(100) NOT NULL,
  -- المدينة — بنستخدمها في الفلترة والبحث
  `image`          VARCHAR(255) DEFAULT NULL COMMENT 'مسار صورة المكان',
  -- مسار صورة المكان
  `price_per_night` DECIMAL(10,2) UNSIGNED NOT NULL DEFAULT 0.00,
  -- سعر الليلة الواحدة — Decimal عشان الفلوس (نقطتين عشريتين)
  `max_guests`     INT UNSIGNED NOT NULL DEFAULT 2,
  -- أقصى عدد ضيوف يقدر يسكنوا
  `category`       VARCHAR(100) NOT NULL DEFAULT 'فندق',
  -- تصنيف المكان: فندق، شقة، منتجع، بيت ضيافة، شاليه، مزرعة
  `rating`         DECIMAL(3,2) UNSIGNED NOT NULL DEFAULT 0.00,
  -- تقييم المكان من 0.00 إلى 5.00
  `is_featured`    TINYINT(1) UNSIGNED NOT NULL DEFAULT 0,
  -- 1 = مكان مقترح يظهر في الصفحة الأولى
  `is_active`      TINYINT(1) UNSIGNED NOT NULL DEFAULT 1,
  -- 1 = المكان متاح، 0 = موقف/مخفي
  `created_at`     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at`     TIMESTAMP NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT `fk_places_company`
    FOREIGN KEY (`company_id`) REFERENCES `companies`(`id`)
    ON DELETE SET NULL ON UPDATE CASCADE,
  -- لو انمسحت الشركة، company_id يصير NULL بدل ما ينمسح المكان
  INDEX `idx_places_company` (`company_id`),
  -- Index لربط الأماكن بالشركات بسرعة
  INDEX `idx_places_city` (`city`)
  -- Index عشان البحث بسرعة حسب المدينة
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 5. جدول الحجوزات
-- ------------------------------------------------------------
-- هنا كل حجز يقوم به الزبون.
-- لاحظ: بنخزّن company_name و place_name كنسخة مطبوعة (denormalized)
-- عشان لو اتغير اسم الشركة أو المكان، الحجز القديم يفضل محتفظ بالاسم الأصلي.
-- العلاقة: Bookings -> Places (مع ON DELETE SET NULL).
-- في CHECK constraint يضمن إن check_out > check_in — ما يصير تاريخ الخروج قبل الدخول.
-- الحالات: pending (قيد الانتظار)، accepted (مقبول)، rejected (مرفوض)، cancelled (ملغي).
-- ------------------------------------------------------------
DROP TABLE IF EXISTS `bookings`;

CREATE TABLE `bookings` (
  `id`             INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `place_id`       INT UNSIGNED NOT NULL,
  -- رقم المكان المحجوز
  `customer_name`  VARCHAR(150) NOT NULL,
  -- اسم الزبون اللي حجز
  `customer_email` VARCHAR(150) DEFAULT NULL,
  `customer_phone` VARCHAR(30)  NOT NULL,
  -- رقم التلفون — إلزامي عشان التواصل
  `company_name`   VARCHAR(150) NOT NULL COMMENT 'نسخة مطبوعة لسهولة العرض',
  -- اسم الشركة وقت الحجز — نسخة احتياطية
  `place_name`     VARCHAR(150) NOT NULL COMMENT 'نسخة مطبوعة لسهولة العرض',
  -- اسم المكان وقت الحجز — نسخة احتياطية
  `check_in`       DATE NOT NULL,
  -- تاريخ الدخول
  `check_out`      DATE NOT NULL,
  -- تاريخ الخروج
  `guests`         INT UNSIGNED NOT NULL DEFAULT 1,
  -- عدد الضيوف الفعلي
  `total_price`    DECIMAL(10,2) UNSIGNED NOT NULL DEFAULT 0.00,
  -- إجمالي السعر = (أيام الإقامة) × (سعر الليلة)
  `status`         ENUM('pending','accepted','rejected','cancelled')
                   NOT NULL DEFAULT 'pending',
  -- حالة الحجز — pending أول مرة، ثم الإداري يقرر
  `notes`          TEXT DEFAULT NULL,
  -- ملاحظات إضافية (مثلاً: طلب إفطار، غرفة معينة)
  `created_at`     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  `updated_at`     TIMESTAMP NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT `chk_dates` CHECK (`check_out` > `check_in`),
  -- نضمن إن تاريخ الخروج بعد تاريخ الدخول
  CONSTRAINT `fk_bookings_place`
    FOREIGN KEY (`place_id`) REFERENCES `places`(`id`)
    ON DELETE SET NULL ON UPDATE CASCADE,
  -- لو انمسح المكان، place_id يصير NULL
  INDEX `idx_bookings_status` (`status`),
  -- Index لتصفية الحجوزات حسب الحالة
  INDEX `idx_bookings_dates` (`check_in`,`check_out`)
  -- Index سريع للبحث حسب التواريخ
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


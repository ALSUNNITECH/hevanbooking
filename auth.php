<?php
/**
 * Hevan Booking - المصادقة وجلسات المستخدم
 * ============================================
 * هنا بنضبط كل حاجة خاصة بالدخول والخروج وأذونات المستخدمين.
 * النظام يستخدم الجلسات (sessions) عشان يتذكر المستخدم بعد ما يسجل دخول،
 * وكل معلومة الجلسة بتتخزن في ملفات عالخادم (server-side)،
 * والمتصفح يحتفظ بس كوكي صغيرة فيها ID الجلسة.
 */

// نجلّب ملف الإعدادات الرئيسي عشان يكون عندنا الاتصال بقاعدة البيانات
require_once __DIR__ . '/config.php';

// نبدأ الجلسة — لازم تكون قبل أي خرج HTML عشان الكوكي تشتغل
session_start();

/**
 * ✅ هل المستخدم إداري؟
 * --------------------------------------------
 * الدالة دي بتتأكد من وجود المستخدم في الجلسة وأن صلاحيته "admin".
 * بنستخدم $_SESSION اللي هي متغير PHP يحتفظ ببيانات الجلسة الحالية.
 * 
 * ■ لو رجعت true: المستخدم إداري وممكن يشوف الصفحات المخصصة للإدارة.
 * ■ لو رجعت false: المستخدم مش إداري أو ما سجلش دخول أساساً.
 */
function is_admin() {
    // $_SESSION['user'] فيه معلومات المستخدم بعد ما سجل دخول
    // role === 'admin' يعني صلاحيته إداري كامل
    return !empty($_SESSION['user']) && $_SESSION['user']['role'] === 'admin';
}

/**
 * 🚫 حماية الصفحات الإدارية
 * --------------------------------------------
 * الدالة دي نستخدمها في بداية أي صفحة إدارية (زي admin/index.php).
 * لو المستخدم مش إداري، توديه تلقائياً لصفحة تسجيل الدخول.
 * 
 * ■ طريقة الاستخدام: نضيف السطر ده في بداية كل صفحة إدارية:
 *   require_admin();
 * 
 * ■ بنستخدم redirect() اللي جاهزة من config.php عشان نوجه المستخدم.
 */
function require_admin() {
    // لو مش إداري → نحوله على طول لـ login.php
    if (!is_admin()) {
        redirect('login.php');
    }
    // لو هو إداري → الدالة تنتهي من غير ما تسوي حاجة، وتكمل الصفحة شغالها
}

/**
 * 🔐 تسجيل دخول المستخدم
 * --------------------------------------------
 * الدالة الأساسية للمصادقة. بتستقبل اسم المستخدم وكلمة السر،
 * وتجيب بيانات المستخدم من قاعدة البيانات وتتأكد من كلمة السر.
 * 
 * ■ خطوات الدالة بالتفصيل:
 *    1. نجهّز استعلام SQL باستخدام prepared statement عشان نمنع حقن SQL.
 *    2. نجيب المستخدم اللي اسمه مطابق و role = 'admin'.
 *    3. نستخدم LIMIT 1 عشان نجيب سجل واحد فقط (لأن اسم المستخدم فريد).
 *    4. نقارن كلمة السر بطريقتين:
 *       - مقارنة مباشرة (عادية) — عشان كلمات السر القديمة المخزنة نص عادي.
 *       - password_verify() — عشان كلمات السر المشفرة بـ bcrypt.
 *    5. لو صح → نخزن بيانات المستخدم في الجلسة ونرجع true.
 *    6. لو غلط → نرجع false بدون ما نحدد السبب بالتحديد (أمان).
 * 
 * ■ ملاحظة أمان: ما نستخدمش مقارنة النص العادي إلا للتوافق مع البيانات القديمة.
 *   أي مستخدم جديد بتتشفّر كلمة سره في ملف database.sql.
 * 
 * @param string $username اسم المستخدم اللي دخل به
 * @param string $password كلمة السر اللي دخل بها
 * @return bool true إذا نجح تسجيل الدخول، false لو فشل
 */
function login_user($username, $password) {
    // نجلّب اتصال قاعدة البيانات من config.php
    global $conn;

    // --- 1. تحضير الاستعلام ---
    // بنستخدم prepared statement: علامة الاستفهام (?) مكان القيمة
    // عشان PDO/MySQLi يفصل الكود عن البيانات ويمنع الحقن
    $stmt = $conn->prepare("SELECT * FROM users WHERE username = ? AND role = 'admin' LIMIT 1");
    
    // --- 2. ربط القيم ---
    // s يعني string (نص) — قيمة اسم المستخدم تربط بعلامة الاستفهام الأولى
    $stmt->bind_param('s', $username);
    
    // --- 3. تنفيذ الاستعلام ---
    $stmt->execute();
    
    // --- 4. نجيب النتيجة كمصفوفة ترابطية (associative array) ---
    // fetch_assoc() ترجع صف كمصفوفة key => value
    $user = $stmt->get_result()->fetch_assoc();

    // --- 5. التحقق من وجود المستخدم وصحة كلمة السر ---
    // نستخدم password_verify عشان كلمات السر bcrypt اللي جاي من password_hash()
    // المقارنة المباشرة ($password === $user['password']) عشان الداتا القديمة
    if ($user && ($password === $user['password'] || password_verify($password, $user['password']))) {
        
        // --- 6. تخزين بيانات المستخدم في الجلسة ---
        // البيانات دي متاحة طول ما الجلسة شغالة في كل الصفحات
        $_SESSION['user'] = [
            'id'       => (int)$user['id'],        // رقم المستخدم — نحوله لرقم صحيح عشان الأمان
            'name'     => $user['name'],           // الاسم الكامل (يظهر في واجهة الإدارة)
            'username' => $user['username'],        // اسم المستخدم للدخول
            'role'     => $user['role'],            // الصلاحية (admin حالياً)
        ];
        return true; // ✅ تسجيل دخول ناجح
    }
    
    // ❌ فشل المصادقة — إما المستخدم غير موجود أو كلمة السر غلط
    return false;
}

/**
 * 🚪 تسجيل الخروج
 * --------------------------------------------
 * الدالة دي ببساطة تمسح بيانات المستخدم من الجلسة وتهدم الجلسة بالكامل.
 * 
 * ■ الفرق بين unset و session_destroy:
 *   - unset($_SESSION['user']): يمسح بس معلومات المستخدم، باقي الجلسة موجودة.
 *   - session_destroy(): يدمر ملف الجلسة كامل عالخادم، لازم تسمعها أخر حاجة.
 * 
 * ■ ليه نستخدم unset قبل session_destroy؟
 *   عشان نتأكد إن أي كود تاني في نفس الصفحة ما يقراش بيانات قديمة من الجلسة
 *   قبل ما ندمرها نهائياً.
 * 
 * ■ بعد استدعاء الدالة، أي محاولة لقراءة $_SESSION['user'] ترجع فارغة،
 *   ووظيفة is_admin() ترجع false.
 */
function logout_user() {
    // نمسح بيانات المستخدم من مصفوفة الجلسة (يبقى الجلسة فاضية)
    unset($_SESSION['user']);
    
    // ندمر الجلسة بالكامل عالخادم — الكوكي اللي في المتصفح ما تعود صالحة
    session_destroy();
}
